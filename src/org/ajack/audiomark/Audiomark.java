package org.ajack.audiomark;

public class Audiomark {
	
	private String shortcode;
	private String timestamp;
	private String title;
	private String subtitle;
	private String url;
	private String radio_station;
	private String radio_station_url;
	private String details;

	public Audiomark() { }
	
	public String getShortcode() { 
		return shortcode;
	}
	
	public void setShortcode(String shortcode) {
		this.shortcode = shortcode;
	}
	
	public String getTimestamp() {
		return timestamp;
	}
	
	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getSubtitle() {
		return subtitle;
	}

	public void setSubtitle(String subtitle) {
		this.subtitle = subtitle;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getRadio_station() {
		return radio_station;
	}

	public void setRadio_station(String radio_station) {
		this.radio_station = radio_station;
	}

	public String getRadio_station_url() {
		return radio_station_url;
	}

	public void setRadio_station_url(String radio_station_url) {
		this.radio_station_url = radio_station_url;
	}

	public String getDetails() {
		return details;
	}

	public void setDetails(String details) {
		this.details = details;
	}
	
	
}
